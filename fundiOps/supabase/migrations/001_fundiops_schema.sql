-- ────────────────────────────────────────────────────────────
-- HELPER: get pending follow-ups due before a timestamp
-- Used by the hourly cron job
-- ────────────────────────────────────────────────────────────

CREATE OR REPLACE FUNCTION get_pending_follow_ups(p_before TIMESTAMPTZ)
RETURNS TABLE (
  id         UUID,
  user_id    UUID,
  contact_id UUID,
  lead_id    UUID,
  note       TEXT,
  due_at     TIMESTAMPTZ
)
LANGUAGE SQL STABLE SECURITY DEFINER AS $$
  SELECT
    f.id, f.user_id, f.contact_id, f.lead_id, f.note, f.due_at
  FROM crm_follow_ups f
  WHERE f.status      = 'pending'
    AND f.reminder_sent = FALSE
    AND f.due_at       <= p_before
  ORDER BY f.due_at ASC
  LIMIT 100;
$$;

-- ────────────────────────────────────────────────────────────
-- AUTO-CREATE profile row on new user signup
-- ────────────────────────────────────────────────────────────

CREATE OR REPLACE FUNCTION handle_new_user()
RETURNS TRIGGER LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
  INSERT INTO profiles (id, email)
  VALUES (NEW.id, NEW.email)
  ON CONFLICT (id) DO NOTHING;
  RETURN NEW;
END;
$$;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION handle_new_user();
